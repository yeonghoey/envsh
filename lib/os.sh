#!/usr/bin/env bash
#
# OS specific functions

################################################################################
# Tests whether current OS is macOS
################################################################################
macos() {
  [[ "$(uname -s)" == "Darwin" ]]
}

################################################################################
# Tests whether current OS is Ubuntu
################################################################################
ubuntu() {
  [[ "$(uname -s)" == "Linux" ]] && [[ -e "/etc/lsb-release" ]]
}
